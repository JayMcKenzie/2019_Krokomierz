void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
   if(huart->Instance == USART3){
	   char odb = receiveUART[0];
	   if(odb == 'Z') {
		   sendStep();
	   }
       HAL_UART_Receive_IT(&huart3, receiveUART, sizeReceiveUART);
   }
}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
   	if(htim->Instance == TIM3)
   	{
   		LIS3DSH_ReadACC(out);
   		accX = out[0];
   		accY = out[1];
   		accZ = out[2];

		if (wyniki < 64){
			float x = accX;
			float y = accY;
			float z = accZ;
			probki[wyniki++] = x+y+z;
		}
		else CzyKrok();
    }
}

void CzyKrok(){

	// Wy³¹czamy timer by moc zrobic odczyt bez kolejnego przerwania
	HAL_TIM_Base_Stop_IT(&htim3);

	uint8_t u = 0;

	for(int i = 0; i < 128; i = i + 2){
		comp[i] = (float) probki[u++];
	}


	// Transformata
	kiss_fft_cfg cfg = kiss_fft_alloc(64, 0, 0, 0);


	kiss_fft(cfg, comp, comp);

	free(cfg);
	comp[0] = 0;


	// Modu³ liczby zespolonej
	arm_cmplx_mag_squared_f32(comp, mag, 64);
	mag[0] = 0;


	// Maksymalna wartosc
	arm_max_f32(mag, 64, &maxvalue, &maxvalueindex);


	if(mag[2] > 50000000){
		if(stop == 0){
			kroki++;
			//sendStep(++kroki);
			stop = 1;
		} else stop = 0;
	} else stop = 0;


	// Porz¹dki
	for(int i=0;i<64;i++){
		probki[i] = 0;
	}

	for(int i=0;i<2*64;i++){
		comp[i] = 0;
	}

	wyniki = 0;


	// W³¹czamy timer
	HAL_TIM_Base_Start_IT(&htim3);
}


void sendStep(){

	// Wyœwietlanie
	//sprintf(buffer,"%d", step);
	krokiCache = kroki;
	HAL_UART_Transmit_IT(&huart3, (uint8_t*)(&krokiCache), 4);
	kroki = 0;
	//tm1637DisplayDecimal(step, 0);
}


